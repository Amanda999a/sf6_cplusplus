// C++THread.cpp : 이 파일에는 'main' 함수가 포함됩니다. 거기서 프로그램 실행이 시작되고 종료됩니다.
//

#include <iostream>
#include <thread>
#include <windows.h> 
using namespace std;

void printMessage(int x) {
    for (int i = 1; i < 11; i ++) {
        cout << i * x << endl;
    }

}
int main()
{
    thread t1(printMessage, 1);
    thread t2(printMessage, 10);
    thread t3(printMessage, 100);
    thread t4(printMessage, 1000);
    thread t5(printMessage, 10000);

    HANDLE handle1 = t1.native_handle();

    SetThreadPriority(handle1, THREAD_PRIORITY_HIGHEST);

    t1.join();
    t2.join();
    t3.join();
    t4.join();
    t5.join();

    return 0;
}

// 프로그램 실행: <Ctrl+F5> 또는 [디버그] > [디버깅하지 않고 시작] 메뉴
