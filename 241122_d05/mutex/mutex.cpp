// mutex.cpp : 이 파일에는 'main' 함수가 포함됩니다. 거기서 프로그램 실행이 시작되고 종료됩니다.
//

#include <iostream>
#include <thread>
#include <vector>
#include <mutex>
using namespace std;

mutex mtx; //뮤텍스 객체 생성
int counter = 0;

void increment() {
    for (int i = 0; i < 10000000; ++i) {
        mtx.lock();//임계 구역에 들어가기 전에 잠금
        ++counter; // 공유 자원에 접근
        mtx.unlock(); // 임계 구역을 벗어나면 잠금 해제
    }
}
int main()
{
    thread t1(increment);
    thread t2(increment);

    t1.join();
    t2.join();

    cout << "Final counter value : " << counter << endl;

    return 0;
}

